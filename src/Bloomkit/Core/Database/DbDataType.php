<?php

namespace Bloomkit\Core\Database;

/**
 * Definition of Database types.
 */
abstract class DbDataType
{
    const Boolean = 'bool';

    const Char = 'char';

    const Date = 'date';

    const Decimal = 'decimal';

    const Integer = 'int';

    const Text = 'text';

    const Time = 'time';

    const Timestamp = 'timestamp';

    const UUID = 'uuid';

    const Varchar = 'varchar';
}
